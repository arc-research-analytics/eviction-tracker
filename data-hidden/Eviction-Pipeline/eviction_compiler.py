def clean_eviction_lab_raw(df: pd.DataFrame, source_label: str = "") -> pd.DataFrame:
    """
    Clean raw Eviction Lab format data (parquet or raw EL CSV).

    Applies the filtering logic from the EL analysis notebook:
    - Non-commercial filings only
    - Non-EL-duplicate filings only
    - Valid geocoding: non-null lat, not low-quality accuracy type

    Derives county_name from xcourtcofips using COUNTY_FIPS_TO_NAME.

    Returns DataFrame with columns: case_id, file_date, county_name, lat, lon
    """
    label = f"[{source_label}] " if source_label else ""
    print(f"  {label}Raw records: {len(df):,}")

    # Non-commercial filings only
    if 'commercial' in df.columns:
        df = df[df['commercial'] == False]

    # Drop EL-flagged duplicates
    if 'dup' in df.columns:
        df = df[df['dup'] == False]

    # Require valid geocoding (non-null latitude)
    if 'GEOID.latitude' in df.columns:
        df = df[df['GEOID.latitude'].notna()]

    # Exclude low-quality accuracy types
    if 'GEOID.accuracy_type' in df.columns:
        df = df[~df['GEOID.accuracy_type'].isin(EL_ACCURACY_EXCLUSION)]

    # Derive county_name from full state+county FIPS (e.g. 13121 → 'Fulton')
    if 'xcourtcofips' in df.columns:
        df = df.copy()
        df['county_name'] = (
            df['xcourtcofips'].astype(str).str.zfill(5).map(COUNTY_FIPS_TO_NAME)
        )

    # Rename to standard column names
    df = df.rename(columns={
        'xcasenum':        'case_id',
        'xfiledate':       'file_date',
        'GEOID.longitude': 'lon',
        'GEOID.latitude':  'lat',
    })

    # Return only the standard columns we need downstream
    keep = [c for c in ['case_id', 'file_date', 'county_name', 'lat', 'lon'] if c in df.columns]
    df = df[keep]

    print(f"  {label}After cleaning: {len(df):,} records")
    return df


def load_eviction_lab_data() -> pd.DataFrame:
    """
    Load all Eviction Lab eviction data.

    Load order:
    1. el_master.csv — compiled historical record (already cleaned)
    2. Parquet files in From_EL/ — raw EL format, cleaned on load
    3. Raw EL CSVs in From_EL/ — detected by xcasenum column, cleaned on load

    After combining:
    - Deduplicates by case_id (handles EL's 6-month lookback overlaps)
    - Filters to filemonth >= 2019-01
    - Exports updated el_master.csv for fast loading next run

    Returns standardized DataFrame with columns:
      case_id, file_date, county_name, lat, lon, filemonth
    """
    print(f"\nLoading Eviction Lab data from: {EVICTION_LAB_DIR}")
    all_dfs = []
    new_el_files = []  # Raw EL files loaded this run — deleted after master is updated

    # 1. Load master CSV (already cleaned — load as-is)
    if EL_MASTER_PATH.exists():
        master_df = pd.read_csv(EL_MASTER_PATH, low_memory=False)
        master_df['lat'] = pd.to_numeric(master_df['lat'], errors='coerce')
        master_df['lon'] = pd.to_numeric(master_df['lon'], errors='coerce')
        all_dfs.append(master_df)
        print(f"  Loaded master: {len(master_df):,} records")

    # 2. Load parquet files (raw EL format)
    for pf in sorted(EVICTION_LAB_DIR.glob("*.parquet")):
        print(f"\n  Loading parquet: {pf.name}")
        raw_df = pd.read_parquet(pf)
        all_dfs.append(clean_eviction_lab_raw(raw_df, pf.name))
        new_el_files.append(pf)

    # 3. Load raw EL CSVs — detect by xcasenum column, skip non-eviction files
    # Also handles legacy processed CSVs (case_id column, no xcasenum) for
    # backfilling historical data — only 2019 records are taken from these,
    # since 2020+ is already covered by the EL parquet/master.
    legacy_county_map = {
        'CLAYTON': 'Clayton', 'COBB': 'Cobb', 'DEKALB': 'DeKalb',
        'GWINNETT': 'Gwinnett', 'FULTON': 'Fulton',
    }
    for csv_file in sorted(EVICTION_LAB_DIR.glob("*.csv")):
        if csv_file.name in EL_SKIP_FILES:
            continue
        peek = pd.read_csv(csv_file, nrows=0)
        if 'xcasenum' in peek.columns:
            print(f"\n  Loading raw EL CSV: {csv_file.name}")
            raw_df = pd.read_csv(csv_file, low_memory=False)
            all_dfs.append(clean_eviction_lab_raw(raw_df, csv_file.name))
            new_el_files.append(csv_file)
        elif 'case_id' in peek.columns:
            print(f"\n  Loading legacy CSV (2019 records only): {csv_file.name}")
            leg_df = pd.read_csv(csv_file, low_memory=False)
            leg_df['lat'] = pd.to_numeric(leg_df['lat'], errors='coerce')
            leg_df['lon'] = pd.to_numeric(leg_df['lon'], errors='coerce')
            leg_df['file_date'] = pd.to_datetime(leg_df['file_date'], errors='coerce')
            leg_df['filemonth'] = leg_df['file_date'].apply(
                lambda x: f"{x.year}-{x.month:02d}" if pd.notna(x) else None
            )
            leg_df = leg_df[leg_df['filemonth'] < '2020-01']
            leg_df['county_name'] = leg_df['county_name'].str.upper().map(legacy_county_map)
            keep = [c for c in ['case_id', 'file_date', 'county_name', 'lat', 'lon', 'filemonth'] if c in leg_df.columns]
            leg_df = leg_df[keep]
            print(f"  [legacy] 2019 records: {len(leg_df):,}")
            all_dfs.append(leg_df)
        else:
            print(f"  Skipping (unrecognized format): {csv_file.name}")

    if not all_dfs:
        print("  No Eviction Lab data found.")
        print("  Place EL parquet or CSV files in the From_EL/ directory.")
        return pd.DataFrame()

    # 4. Combine and deduplicate by case_id
    combined = pd.concat(all_dfs, ignore_index=True)
    before = len(combined)
    combined = combined.drop_duplicates(subset=['case_id'])
    print(f"\n  Combined: {before:,} → {len(combined):,} records "
          f"({before - len(combined):,} duplicates removed)")

    # 5. Validate coordinates
    combined['lat'] = pd.to_numeric(combined['lat'], errors='coerce')
    combined['lon'] = pd.to_numeric(combined['lon'], errors='coerce')
    before = len(combined)
    combined = combined[
        combined['lat'].notna() & combined['lon'].notna() &
        (combined['lat'] != 0) & (combined['lon'] != 0)
    ]
    if before != len(combined):
        print(f"  Dropped {before - len(combined):,} records with invalid coordinates")

    # 6. Generate filemonth in "YYYY-MM" format
    combined['file_date'] = pd.to_datetime(combined['file_date'], errors='coerce')
    combined['filemonth'] = combined['file_date'].apply(
        lambda x: f"{x.year}-{x.month:02d}" if pd.notna(x) else None
    )
    combined = combined.dropna(subset=['filemonth'])

    # 7. Filter to 2019-01 and later
    before = len(combined)
    combined = combined[combined['filemonth'] >= '2019-01']
    if before != len(combined):
        print(f"  Filtered {before - len(combined):,} records before 2019-01")

    # 8. Export updated master CSV
    export_df = combined.copy()
    export_df['file_date'] = export_df['file_date'].dt.strftime('%Y-%m-%d')
    export_df.to_csv(EL_MASTER_PATH, index=False)

    print(f"\n  Updated el_master.csv: {len(export_df):,} records")
    print(f"  Date range: {combined['filemonth'].min()} → {combined['filemonth'].max()}")
    print(f"  Final records: {len(combined):,}")

    # Delete raw EL files now that el_master.csv is updated
    if new_el_files:
        print(f"\n  Cleaning up {len(new_el_files)} raw EL file(s):")
        for f in new_el_files:
            f.unlink()
            print(f"    Deleted: {f.name}")

    return combined



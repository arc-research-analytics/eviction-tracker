def push_to_supabase(aggregated_data: dict) -> bool:
    """
    Push aggregated data to Supabase tables.

    Tables:
    - evictions-tract:  Tract-level aggregations
    - evictions-hex:    Hexagon-level aggregations
    - evictions-school: School-level aggregations
    - evictions-city:   City-level aggregations
    - evictions-county: County-level aggregations (for trends chart)

    Returns True if all uploads succeeded, False if any errors occurred.
    """
    print("\n" + "=" * 60)
    print("PUSHING TO SUPABASE")
    print("=" * 60)

    if SKIP_SUPABASE:
        print("  Supabase upload SKIPPED (SKIP_SUPABASE=True)")
        print("  Set SKIP_SUPABASE=False when ready to upload")
        return True

    if not SUPABASE_URL or not SUPABASE_KEY:
        print("  ERROR: Supabase credentials not configured!")
        print("  Set SUPABASE_URL and SUPABASE_KEY in the .env file")
        return False

    supabase: Client = create_client(SUPABASE_URL, SUPABASE_KEY)
    all_succeeded = True

    table_mapping = {
        'tracts':   'evictions-tract',
        'hexagons': 'evictions-hex',
        'schools':  'evictions-school',
        'cities':   'evictions-city',
        'counties': 'evictions-county',
    }

    # tract_id → tractid for Supabase schema compatibility
    column_mapping = {
        'tracts': {'tract_id': 'tractid'},
    }

    for data_type, table_name in table_mapping.items():
        if data_type not in aggregated_data:
            continue

        df = aggregated_data[data_type].copy()
        print(f"\n  Uploading to {table_name}: {len(df):,} rows")

        if data_type in column_mapping:
            df = df.rename(columns=column_mapping[data_type])

        # Sanitize NaN/inf before JSON serialization
        df = df.replace([float('inf'), float('-inf')], None)
        df = df.where(pd.notna(df), None)

        records = df.to_dict('records')
        for record in records:
            record['id'] = str(uuid.uuid4())
            for key, value in record.items():
                if isinstance(value, float) and (math.isnan(value) or math.isinf(value)):
                    record[key] = None

        # Delete all existing rows, then insert fresh data
        print(f"    Deleting existing data from {table_name}...")
        try:
            supabase.table(table_name).delete().neq('id', '00000000-0000-0000-0000-000000000000').execute()
            print(f"    Deleted successfully")
        except Exception as e:
            print(f"    WARNING: Could not delete existing data: {e}")
            all_succeeded = False

        # Insert in batches
        batch_size    = 1000
        total_uploaded = 0
        for i in range(0, len(records), batch_size):
            batch = records[i:i + batch_size]
            try:
                supabase.table(table_name).insert(batch).execute()
                total_uploaded += len(batch)
                print(f"    Batch {i // batch_size + 1}: {len(batch)} records inserted")
            except Exception as e:
                print(f"    ERROR on batch {i // batch_size + 1}: {e}")
                all_succeeded = False

        print(f"    Total uploaded: {total_uploaded:,}")

    return all_succeeded



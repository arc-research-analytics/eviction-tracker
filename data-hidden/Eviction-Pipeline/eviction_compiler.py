def aggregate_by_geography(df: pd.DataFrame, rocc_data: dict = None) -> dict:
    """
    Aggregate eviction counts by geography and month.

    Expects df to have 'filemonth' in "YYYY-MM" format.

    Returns dict of DataFrames keyed by geography type:
      tracts, hexagons, schools, cities, counties
    """
    print("\n" + "=" * 60)
    print("AGGREGATING DATA")
    print("=" * 60)

    if rocc_data is None:
        rocc_data = {'hex': {}, 'tract': {}, 'school': {}, 'city': {}}

    results = {}

    # Aggregate for each geography type
    for id_col, result_key, rocc_key, label in [
        ('tract_id',  'tracts',   'tract',  'Tract'),
        ('hex_id',    'hexagons', 'hex',    'Hexagon'),
        ('school_id', 'schools',  'school', 'School'),
        ('city_id',   'cities',   'city',   'City'),
    ]:
        if id_col not in df.columns:
            continue

        # pandas groupby drops NaN keys by default, so unmatched records are excluded
        agg = df.groupby([id_col, 'filemonth']).agg(
            totalfilings=(id_col, 'count')
        ).reset_index()

        if rocc_data.get(rocc_key):
            agg = calculate_filing_rate(agg, id_col, rocc_data[rocc_key])
            matched = agg['filing-rate'].notna().sum()
            print(f"  {label}: {len(agg):,} rows ({matched:,} with filing rates)")
        else:
            agg['filing-rate'] = None
            print(f"  {label}: {len(agg):,} rows (no ROcc data)")

        results[result_key] = agg

    # County aggregation derived from tract data for perfect alignment
    # County FIPS = first 5 digits of tract GEOID
    if 'tract_id' in df.columns:
        county_df = df[df['tract_id'].notna()].copy()
        county_df['county_fips'] = county_df['tract_id'].str[:5]
        county_df = county_df[county_df['county_fips'].isin(COUNTY_FIPS_TO_NAME)]
        county_df['county_name'] = county_df['county_fips'].map(COUNTY_FIPS_TO_NAME)
        county_agg = county_df.groupby(['county_name', 'filemonth']).agg(
            totalfilings=('county_name', 'count')
        ).reset_index()
        results['counties'] = county_agg
        print(f"  County: {len(county_agg):,} rows (derived from tract data)")

    return results



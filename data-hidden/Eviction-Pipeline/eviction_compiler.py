def create_geodataframe(df: pd.DataFrame) -> gpd.GeoDataFrame:
    """Convert DataFrame with lat/lon columns to a GeoDataFrame."""
    valid = df.dropna(subset=['lat', 'lon']).copy()
    if len(valid) == 0:
        return gpd.GeoDataFrame()
    geometry = [Point(xy) for xy in zip(valid['lon'], valid['lat'])]
    return gpd.GeoDataFrame(valid, geometry=geometry, crs="EPSG:4326")


def spatial_join_to_geography(
    evictions_gdf: gpd.GeoDataFrame,
    geography_path: Path,
    id_column: str,
    output_name: str,
) -> gpd.GeoDataFrame:
    """
    Perform a spatial join to assign evictions to geographic areas.

    Args:
        evictions_gdf:   GeoDataFrame of eviction points
        geography_path:  Path to GeoJSON file with polygons
        id_column:       Column in the geography file to use as identifier
        output_name:     Name for the output column (e.g., 'tract_id')

    Returns GeoDataFrame with the geography ID column added.
    """
    print(f"\n  Joining to: {geography_path.name}")

    if not geography_path.exists():
        print(f"    WARNING: File not found: {geography_path}")
        return evictions_gdf

    geography = gpd.read_file(geography_path)
    print(f"    Loaded {len(geography)} geographic units")

    if geography.crs != evictions_gdf.crs:
        geography = geography.to_crs(evictions_gdf.crs)

    joined = gpd.sjoin(
        evictions_gdf,
        geography[[id_column, 'geometry']],
        how='left',
        predicate='within',
    )

    if id_column in joined.columns:
        joined = joined.rename(columns={id_column: output_name})

    if 'index_right' in joined.columns:
        joined = joined.drop(columns=['index_right'])

    matched = joined[output_name].notna().sum() if output_name in joined.columns else 0
    print(f"    Matched: {matched:,} records")

    return joined


def perform_spatial_joins(df: pd.DataFrame) -> pd.DataFrame:
    """
    Perform spatial joins to tracts, hexagons, schools, and cities.

    Uses high-resolution GeoJSON files from data-hidden/ for accurate
    attribution. County aggregations are derived from tract data rather
    than a separate spatial join, ensuring perfect alignment.
    """
    print("\n" + "=" * 60)
    print("PERFORMING SPATIAL JOINS")
    print("=" * 60)

    gdf = create_geodataframe(df)

    if gdf.empty:
        print("  No geocoded records to process!")
        return df

    print(f"  Records for spatial join: {len(gdf):,}")

    gdf = spatial_join_to_geography(gdf, GEOJSON_TRACTS,  id_column='GEOID',      output_name='tract_id')
    gdf = spatial_join_to_geography(gdf, GEOJSON_HEX,     id_column='hex_id',     output_name='hex_id')
    gdf = spatial_join_to_geography(gdf, GEOJSON_SCHOOLS, id_column='ShortLabel', output_name='school_id')
    gdf = spatial_join_to_geography(gdf, GEOJSON_CITIES,  id_column='ShortLabel', output_name='city_id')

    result = pd.DataFrame(gdf.drop(columns=['geometry']))

    print(f"\n  Records outside boundaries:")
    print(f"    Outside tracts:  {result['tract_id'].isna().sum():,}")
    print(f"    Outside schools: {result['school_id'].isna().sum():,}")
    print(f"    Outside hexes:   {result['hex_id'].isna().sum():,}")
    print(f"    Outside cities:  {result['city_id'].isna().sum():,}")

    return result



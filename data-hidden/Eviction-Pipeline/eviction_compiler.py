def git_commit_and_push(max_date: str):
    """
    Stage js/config.template.js, commit, and push to remote.

    data-hidden/ is gitignored, so this commit only captures the MAX_DATE
    update in config.template.js — a small, meaningful change.
    """
    print("\n" + "=" * 60)
    print("GIT COMMIT AND PUSH")
    print("=" * 60)

    try:
        # Stage config files (config.js is the production file served by GitHub Pages)
        result = subprocess.run(
            ['git', 'add', 'js/config.template.js', 'js/config.js'],
            cwd=PROJECT_ROOT, capture_output=True, text=True
        )
        if result.returncode != 0:
            print(f"  ERROR: git add failed: {result.stderr.strip()}")
            return

        # Check if there's anything to commit
        result = subprocess.run(
            ['git', 'diff', '--cached', '--quiet'],
            cwd=PROJECT_ROOT, capture_output=True
        )
        if result.returncode == 0:
            print("  No changes to commit (config was already up to date)")
            return

        # Commit
        commit_msg = f"data: update MAX_DATE to {max_date}"
        result = subprocess.run(
            ['git', 'commit', '-m', commit_msg],
            cwd=PROJECT_ROOT, capture_output=True, text=True
        )
        if result.returncode != 0:
            print(f"  ERROR: git commit failed: {result.stderr.strip()}")
            return
        print(f"  Committed: {commit_msg}")

        # Push
        result = subprocess.run(
            ['git', 'push'],
            cwd=PROJECT_ROOT, capture_output=True, text=True
        )
        if result.returncode != 0:
            print(f"  ERROR: git push failed: {result.stderr.strip()}")
            print("  Push manually: git push")
            return
        print("  Pushed to remote successfully")

    except FileNotFoundError:
        print("  ERROR: git not found in PATH")



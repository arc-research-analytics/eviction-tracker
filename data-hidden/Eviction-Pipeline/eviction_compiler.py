"""
Eviction Data Compiler

This script loads and cleans Eviction Lab data, performs spatial joins,
and pushes aggregated results to Supabase. It also determines the latest
complete month of data and updates the frontend JavaScript config files.

PIPELINE:
1. Load Eviction Lab data:
   a. el_master.csv — compiled historical record (already cleaned, fast to load)
   b. Parquet files in From_EL/ — raw EL format, cleaned on load
   c. Raw EL CSVs in From_EL/ with xcasenum column — cleaned on load
2. Deduplicate by case_id, filter to 2019-01+, export updated el_master.csv
3. Perform spatial joins to tracts, hexagons, schools, and cities
4. Load renter occupied housing units data for filing rate calculation
5. Aggregate eviction counts and filing rates by geography and month
6. Determine MAX_DATE (latest month with apparently complete data)
7. Update frontend config files (js/config*.js) with new MAX_DATE/DEFAULT_DATE
8. Push aggregated data to Supabase tables

MONTHLY WORKFLOW:
1. Drop new EL file (parquet or CSV) into From_EL/
2. Run this script
   - el_master.csv is updated automatically
   - Raw EL file is deleted automatically
   - git commit + push happens automatically (if Supabase succeeded)

Data Sources:
- Eviction Lab: ./From_EL/  (parquet or raw EL CSVs with xcasenum column)
- Compiled master: ./From_EL/el_master.csv (auto-generated, cleaned, deduplicated)

Spatial Joins (high-res GeoJSON in data-hidden/ for accuracy):
- Tracts:   region_tracts_hires.geojson
- Schools:  region_schools_hires.geojson
- Hexagons: region_hex.geojson (in data/, H3 structure doesn't need simplification)

County Aggregation:
- Derived from tract data (county FIPS = first 5 chars of tract GEOID)
- Ensures perfect alignment: sum of tract totals = county total

Frontend Config Update:
- MAX_DATE:     latest month with >= 70% of trailing average filing count
- DEFAULT_DATE: 12 months prior to MAX_DATE

Output:
- Updated el_master.csv in From_EL/
- Updated js/config*.js files
- Aggregated data pushed to Supabase
"""


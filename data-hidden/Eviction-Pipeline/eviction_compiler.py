def load_rocc_data() -> dict:
    """
    Load renter occupied housing unit data for all geographies.

    Returns dict with keys 'hex', 'tract', 'school', 'city', each mapping
    geography ID → ROcc_HUs count.
    """
    print("\n" + "=" * 60)
    print("LOADING RENTER OCCUPIED HOUSING UNITS DATA")
    print("=" * 60)

    rocc_data = {}
    for key, path, label in [
        ('hex',    ROCC_HEX_PATH,    'Hex'),
        ('tract',  ROCC_TRACT_PATH,  'Tract'),
        ('school', ROCC_SCHOOL_PATH, 'School'),
        ('city',   ROCC_CITY_PATH,   'City'),
    ]:
        if path.exists():
            df = pd.read_csv(path)
            rocc_col = 'ROcc_HUs' if 'ROcc_HUs' in df.columns else 'ROcc_Hus'
            rocc_data[key] = dict(zip(df['ShortLabel'].astype(str), df[rocc_col]))
            print(f"  {label} ROcc: {len(rocc_data[key]):,} records from {path.name}")
        else:
            print(f"  WARNING: {label} ROcc file not found: {path}")
            rocc_data[key] = {}

    return rocc_data


def calculate_filing_rate(df: pd.DataFrame, id_column: str, rocc_lookup: dict) -> pd.DataFrame:
    """Calculate filing rate (totalfilings / ROcc_HUs) for each row."""
    def calc_rate(row):
        geo_id = str(row[id_column])
        rocc = rocc_lookup.get(geo_id, 0)
        if rocc and rocc > 0:
            return round(row['totalfilings'] / rocc, 6)
        return None

    df['filing-rate'] = df.apply(calc_rate, axis=1)
    return df


